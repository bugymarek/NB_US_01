    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Double sum = Double.parseDouble(jTextField11.getText().replace(',', '.'));
        ArrayList<Double> shareArr = new ArrayList<>();
        for (int i = 0; i < dtm.getRowCount(); i++) {
            String result = Main.tryParseToDouble(dtm.getValueAt(i, 1).toString());
            if (result != null) {
                JOptionPane.showMessageDialog(this,
                        result,
                        "Pozor",
                        JOptionPane.WARNING_MESSAGE);
                return;
            }
            Double share = Double.parseDouble(dtm.getValueAt(i, 1).toString().replace(',', '.'));
            shareArr.add(share);
            sum += share;
        }

        if (sum != 100.0) {
            JOptionPane.showMessageDialog(this,
                    "Suma podielov musí byť 100%",
                    "Pozor",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        ArrayList<String> rcArr = new ArrayList<>();
        for (int i = 0; i < dtm.getRowCount(); i++) {
            rcArr.add(dtm.getValueAt(i, 0).toString());
        }

        fillResponsData(shareArr, rcArr);
        cancel = false;
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

