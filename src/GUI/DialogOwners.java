    private void fillData() {
        jTextField1.setEnabled(false);
        jTextField11.setEnabled(false);

        if (owners != null && owners.get("editedOwner") != null) {
            this.jTextField1.setText(owners.get("editedOwner").getAsJsonObject().get("rc").getAsString());
            this.jTextField11.setText(owners.get("editedOwner").getAsJsonObject().get("share").getAsString());
        }
        String[] header = new String[]{"Rodné čislo", "Podiel v %"};
        dtm.setColumnIdentifiers(header);
        jTableOwnerships.setModel(dtm);
        if (owners != null && owners.get("ownerships") != null) {
            for (JsonElement jsonElement : owners.get("ownerships").getAsJsonArray()) {
                JsonObject owner = (JsonObject) jsonElement;
                dtm.addRow(new Object[]{owner.get("rc").getAsString(), owner.get("share").getAsString()});
            }
        }
    }

    private void fillResponsData(ArrayList<Double> arrDouble, ArrayList<String> rcArr) {
        JsonArray jsonArray = new JsonArray();
        owners.get("editedOwner").getAsJsonObject().addProperty("share", Double.parseDouble(jTextField11.getText().replace(',', '.')));
        for (int i = 0; i < arrDouble.size(); i++) {
            JsonObject jo = new JsonObject();
            jo.addProperty("rc", rcArr.get(i));
            jo.addProperty("share", arrDouble.get(i));
            jsonArray.add(jo);
        }
        owners.add("ownerships", jsonArray);
    }

